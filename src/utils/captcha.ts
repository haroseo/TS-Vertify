export function generateCaptcha() {
    const width = 250;
    const height = 100;
    const canvas = createCanvas(width, height);
    const ctx = canvas.getContext('2d');

    // 배경색 채우기 (깔끔한 어두운 회색)
    ctx.fillStyle = '#1e1f22';
    ctx.fillRect(0, 0, width, height);

    // 랜덤 텍스트 생성
    const chars = Object.keys(FONT_DATA);
    let text = '';
    for (let i = 0; i < 6; i++) {
        text += chars[Math.floor(Math.random() * chars.length)];
    }

    // 픽셀 폰트 그리기 로직
    const pixelSize = 8; // 각 픽셀의 크기
    const startX = 25;
    const startY = 22;
    const spacing = 35;

    ctx.fillStyle = '#ffffff'; // 글자 색상 (완전 하얀색)

    for (let i = 0; i < text.length; i++) {
        const char = text[i];
        const data = FONT_DATA[char];
        if (!data) continue;

        const currentX = startX + (i * spacing);

        for (let row = 0; row < data.length; row++) {
            for (let col = 0; col < data[row].length; col++) {
                if (data[row][col] === 1) {
                    // 점 하나하나를 직접 그리기 (폰트 사용 안함)
                    ctx.fillRect(
                        currentX + (col * pixelSize), 
                        startY + (row * pixelSize), 
                        pixelSize - 1, 
                        pixelSize - 1
                    );
                }
            }
        }
    }

    return {
        text,
        image: canvas.toBuffer()
    };
}
