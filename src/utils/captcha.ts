export function generateCaptcha() {
    const width = 300; // 넓이 증가
    const height = 100;
    const canvas = createCanvas(width, height);
    const ctx = canvas.getContext('2d');

    // 배경색 채우기 (깔끔한 어두운 회색)
    ctx.fillStyle = '#1e1f22';
    ctx.fillRect(0, 0, width, height);

    // 랜덤 텍스트 생성 (가독성 고려한 필터링)
    const filteredChars = Object.keys(FONT_DATA).filter(c => !['L', 'W', 'M'].includes(c)); // 가로로 너무 긴 글자 제외
    let text = '';
    for (let i = 0; i < 6; i++) {
        text += filteredChars[Math.floor(Math.random() * filteredChars.length)];
    }

    // 픽셀 폰트 그리기 로직 (겹침 방지)
    const pixelSize = 6; // 픽셀 크기 약간 축소
    const startX = 25;
    const startY = 30;
    const spacing = 45; // 자간 대폭 확대 (겹침 방지)

    for (let i = 0; i < text.length; i++) {
        const char = text[i];
        const data = FONT_DATA[char];
        if (!data) continue;

        const currentX = startX + (i * spacing);
        
        // 글자별로 다른 색상 적용 (가독성 향상)
        ctx.fillStyle = `hsl(${i * 60}, 70%, 75%)`;

        for (let row = 0; row < data.length; row++) {
            for (let col = 0; col < data[row].length; col++) {
                if (data[row][col] === 1) {
                    ctx.fillRect(
                        currentX + (col * pixelSize), 
                        startY + (row * pixelSize), 
                        pixelSize - 1, 
                        pixelSize - 1
                    );
                }
            }
        }
    }

    return {
        text,
        image: canvas.toBuffer()
    };
}
